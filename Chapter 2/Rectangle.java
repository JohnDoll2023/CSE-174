public class Rectangle
{
   public static void main(String [] args)
   {
      //Declaring variables
      double width = 9.5;
      double height = 3.1;
         
      //Writing formulas
      double area = width*height;
      double perimeter = 2*(width+height);
      System.out.println("Area: " + area + "\nPerimeter: " + perimeter);
   }
}
      
         